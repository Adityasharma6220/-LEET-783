package ArraysDemo;

public class SumOfAllElement {

	public static void main(String[] args) {
//WAP to get the sum of all elements in the array
		  int [] a ={20,30,50};	
		int  sum=0;
		for(int i=0;i<a.length;i++) {  
 sum=sum+a[i];			
		}
	System.out.println("The sum is " +sum);
	}
	
}
